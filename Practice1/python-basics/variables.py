# Example 1:
# Assigning an integer value to a variable
x = 5
print(x)

# Example 2:
# Assigning a string value to a variable
name = "Aru"
print(name)

# Example 3:
# Variables can change their type
x = 10
x = "ten"
print(x)

# Example 4:
# Multiple variables can be assigned in one line
a, b, c = 1, 2, 3
print(a, b, c)

# Example 5:
# Global variable used inside a function
x = "global"

def func():
    print(x)

func()
