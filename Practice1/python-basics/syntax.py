# Example 1:
# If statement executes code only if the condition is True
if 10 > 5:
    print("Correct syntax")

# Example 2:
# For loop repeats code a specific number of times
for i in range(3):
    print(i)

# Example 3:
# Variable comparison using equality operator
x = 5
if x == 5:
    print("x is equal to 5")

# Example 4:
# While loop repeats while condition is True
while x > 2:
    print(x)
    x -= 1  # decreases x by 1

# Example 5:
# Boolean expression returns True or False
print(True)
