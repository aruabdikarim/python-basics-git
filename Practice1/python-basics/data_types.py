# Example 1:
# Checking the data type of a variable
x = 5
print(type(x))

# Example 2:
# List stores multiple values in one variable
my_list = ["apple", "banana"]
print(my_list)

# Example 3:
# Tuple is an ordered and unchangeable collection
my_tuple = ("red", "blue")
print(my_tuple)

# Example 4:
# Dictionary stores data in key-value pairs
my_dict = {"name": "Aru", "age": 18}
print(my_dict)

# Example 5:
# Boolean expression evaluates to True or False
print(10 > 5)
