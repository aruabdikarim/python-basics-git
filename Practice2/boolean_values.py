# Example 1: Basic boolean values
print(True)
print(False)

# Example 2: bool() from numbers
print(bool(1))   # True
print(bool(0))   # False

# Example 3: bool() from strings
print(bool("Hello"))  # True
print(bool(""))       # False

# Example 4: bool() from collections
print(bool([1, 2, 3]))  # True
print(bool([]))         # False
