# Example 1
a = 50
b = 20
if a > b:
    print("a is greater")
else:
    print("b is greater")

# Example 2: Even or odd
n = 7
if n % 2 == 0:
    print("Even")
else:
    print("Odd")

# Example 3: Password length
password = "abc123"
if len(password) >= 6:
    print("Password OK")
else:
    print("Password too short")

# Example 4: Balance check
balance = 100
cost = 120
if cost <= balance:
    print("Purchase accepted")
else:
    print("Not enough money")

# Example 5: Empty string
text = ""
if text:
    print("Not empty")
else:
    print("Empty")
