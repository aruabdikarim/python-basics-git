# Example 1: and operator
x = 10
print(x > 5 and x < 20)  # True

# Example 2: or operator
age = 16
print(age < 18 or age > 60)  # True

# Example 3: not operator
is_raining = False
print(not is_raining)  # True

# Example 4: Combined operators
print((5 > 0 and 0 > 1) or True)  # True

# Example 5: Boolean expression
score = 70
print(score >= 50 and score <= 100)  # True
