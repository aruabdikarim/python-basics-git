# Example 1: Simple if
a = 10
b = 20
if b > a:
    print("b is greater than a")

# Example 2: Temperature check
temp = 30
if temp >= 25:
    print("It is warm")

# Example 3: Positive number
num = 5
if num > 0:
    print("Positive number")

# Example 4: Check list value
fruits = ["apple", "banana"]
if "banana" in fruits:
    print("Banana found")

# Example 5: String length
name = "Ali"
if len(name) >= 3:
    print("Valid name")
